package Bitwiseshift;

public class Main {
    public static void main(String args[])  {  
        int a = 10;  
        System.out.println(a>>1);     //right shift half
        System.out.println(a>>2);     //right shift half half
        System.out.println(a<<1);     //left shift double
        System.out.println(a<<2);     //left shift double double
     }      
}


